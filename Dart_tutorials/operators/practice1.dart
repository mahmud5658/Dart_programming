void main() {
  int num1 = 10;
  int num2 = 20;

  int add = num1 + num2;
  int sub = num1 - num2;
  int multi = num1 * num2;
  double div = num1 / num2;
  int div2 = num1 ~/ num2;
  int mod = num1 % num2;
  int unaryMinus = -num2;

  // display information

  print("The sum is: $add");
  print("The difference is: $sub");
  print("The multiplication is: $multi");
  print("The division is: $div");
  print("The div2 is: $div2");
  print("The modulus is: $mod");
  print("The unary operation is: $unaryMinus");
}
